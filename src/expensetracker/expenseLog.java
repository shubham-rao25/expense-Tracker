    void load_name(){
       try{
           PreparedStatement pstmt=con.prepareStatement("select * from funds");
           ResultSet rs=pstmt.executeQuery();
           combo.removeAllItems();
           while(rs.next()){
               combo.addItem(rs.getString("name"));
           }
       }catch(Exception e){
           e.printStackTrace();
       }
   }
