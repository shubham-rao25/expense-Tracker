    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=combo.getSelectedItem().toString();
        String month=combo1.getSelectedItem().toString();
        String date=txtdate.getText();
        try{
        PreparedStatement pstmt=con.prepareStatement("select * from daily where name=? and month=? and date=?");
        pstmt.setString(1, name);
        pstmt.setString(2, month);
        pstmt.setString(3, date);
        ResultSet rs=pstmt.executeQuery();
        if(rs.next()==true){
            txtarea.setText(txtarea.getText()+"EXPENSE MADE BY :"+name+"\n");
            txtarea.setText(txtarea.getText()+"MONTH :"+month+"\n");
            txtarea.setText(txtarea.getText()+"DATE :"+date+"\n");
            txtarea.setText(txtarea.getText()+"EXPENSE:"+rs.getString("amount")+"\n");
        }
        JOptionPane.showMessageDialog(null, "EXPENSES SEARCHED SUCCESSFULLY!!!!");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

